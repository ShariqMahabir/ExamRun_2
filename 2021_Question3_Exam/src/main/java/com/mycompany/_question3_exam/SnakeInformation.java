    // Method to read from snakes.txt
    private void loadSnakeNames() {
   try (BufferedReader br = new BufferedReader(new FileReader("snakes.txt"))) {
        String line;
        jComboBox1.removeAllItems();
        jComboBox1.addItem("Select Snake"); // default

        while ((line = br.readLine()) != null) {
            // If your file is CSV (one record per line): "Name,Rank,Assistance"
            String[] parts = line.split(",");
            if (parts.length > 0) {
                jComboBox1.addItem(parts[0].trim()); // add name only
            }
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error loading snakes: " + e.getMessage());
    }
        
}

    

