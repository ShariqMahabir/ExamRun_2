    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Method for the process button
         String selectedSnake = (String) jComboBox1.getSelectedItem();
    if (selectedSnake == null || selectedSnake.equals("Select Snake")) {
        JOptionPane.showMessageDialog(this, "Please select a snake first!");
        return;
    }

    // clear previous output
    jTextArea1.setText("");

    try (BufferedReader br = new BufferedReader(new FileReader("snakes.txt"))) {
        String line;
        boolean found = false;

        while ((line = br.readLine()) != null) {
            String[] parts = line.split(",");
            if (parts.length >= 3 && parts[0].equalsIgnoreCase(selectedSnake)) {
                found = true;
                jTextArea1.append("Snake: " + parts[0].trim() + "\n");
                jTextArea1.append("Fatality Ranking: " + parts[1].trim() + "\n");
                jTextArea1.append("Medical Attention: " + parts[2].trim() + "\n");
                break;
            }
        }

        if (!found) {
            jTextArea1.setText("Information not found for: " + selectedSnake);
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error reading file: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

